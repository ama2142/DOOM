#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned long long	i;
	unsigned char		*tmp_dst;
	unsigned char		*tmp_src;

	if ((dst == NULL) && (src == NULL))
		return (NULL);
	tmp_dst = (unsigned char *)dst;
	tmp_src = (unsigned char *)src;
	i = 0;
	while (i < n)
	{
		tmp_dst[i] = tmp_src[i];
		i++;
	}
	return (dst);
}
